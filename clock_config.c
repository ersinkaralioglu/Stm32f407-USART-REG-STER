#include "clock_config.h"
void SysClockConfig (void)
{
RCC->CR|=0X00030000;
while(!(RCC->CR & 0x00020000));
RCC->CR|=0x00080000;
RCC->PLLCFGR|=0X00400000;
RCC->PLLCFGR|=0X00000004;
RCC->PLLCFGR|=0X00005A00;
RCC->PLLCFGR|=0X00000000;
RCC->CFGR|=0X00000000;
RCC->CFGR|=0X00080000;
RCC->CFGR|=0X00001400;
RCC->CIR|=0X00080000;
RCC->CIR|=0X00800000;
}
